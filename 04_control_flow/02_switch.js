// switch case statement

// syntax
/*
switch (key) {
    case value:
        
        break;

    default:
        break;
}
*/

const month = "March"

switch (month) {
    case "Jan":
        console.log("January")
        break;
    case "Feb":
        console.log("February")
        break;
    case "March":
        console.log("March")
        break;
    case "April":
        console.log("April")

    default:
        console.log("default case match")
        break;
}

// cases can be written in numbers and strings